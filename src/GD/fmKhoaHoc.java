    private void Inlist(){
        DefaultListModel<String> model= new DefaultListModel<>();
        List<GiangVien> listgv = kh.getListgiangvien();
        for(var o : listgv){
            model.addElement(o.getTen() + " " + o.getSdt());
        }
        lsgv.setModel(model);
    }
    private void InTable(){
        List<HocVien>listH = kh.getListhocvien();
//        System.out.println(listH.size());
        Object data[][]= new Object[listH.size()][5];
        for(int i =0 ; i< listH.size(); i++){
            HocVien hocvien =listH.get(i);
            data[i][0]= hocvien.getTen();
            data[i][1]= hocvien.getNgaysinh();
            data[i][2]= hocvien.getGioitinh();
            data[i][3]=hocvien.getQue();
            data[i][4]=hocvien.getCccd();
        }
        String[] columnames= {"Tên","Ngày Sinh","Giới tính","Quê quán","CCCD"};
        DefaultTableModel model = new DefaultTableModel(data,columnames);
        tbHocVien.setModel(model);
    }

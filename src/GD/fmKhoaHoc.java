    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String ten = JOptionPane.showInputDialog(null,"Nhập 1.Xóa học viên, 2. Xóa giảng viên",JOptionPane.PLAIN_MESSAGE);
        if(ten.equals("1")){
            String tenn = JOptionPane.showInputDialog(null, "Nhập tên cần xóa:", "Xóa học viên", JOptionPane.PLAIN_MESSAGE);
            String tenlow= tenn.toLowerCase();
            if (tenn != null && !tenn.isEmpty()) {
                List<HocVien> hocVienList = kh.getListhocvien();
                Iterator<HocVien> iterator = hocVienList.iterator();
                boolean found = false; // Cờ để xác định xem có tìm thấy sinh viên hay không

                while (iterator.hasNext()) {
                    HocVien hocVien = iterator.next();
                    String s= hocVien.getTen().toLowerCase();
                    if (s.equals(tenlow)) {
                        iterator.remove();
                        found = true;
                    }
                }
                QuanLiMain.SaveData();

                if (found) {
                    // Cập nhật lại JTable hoặc model
                    DefaultTableModel model = (DefaultTableModel) tbHocVien.getModel();
                    model.setRowCount(0); // Xóa tất cả các dòng trong model

                    for (HocVien hocVien : hocVienList) {
                        Object[] row = {hocVien.getTen(), hocVien.getNgaysinh(), hocVien.getGioitinh(),hocVien.getQue(),hocVien.getCccd()};
                        model.addRow(row); // Thêm lại dữ liệu vào model
                    }
                } else {
                    // Hiển thị thông báo khi không tìm thấy sinh viên
                    JOptionPane.showMessageDialog(null, "Không tìm thấy học viên tên: " + tenn, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        
        }else if(ten.equals("2")){
            String tengv= JOptionPane.showInputDialog(null,"Nhập tên giảng viên cần xóa:",JOptionPane.PLAIN_MESSAGE);
            String tenlow =tengv.toLowerCase();
            if(tengv!=null && !tengv.isEmpty()){
                List<GiangVien> listgv= kh.getListgiangvien();
                Iterator<GiangVien> iterator =listgv.iterator();
                boolean found= false;
                while(iterator.hasNext()){
                    GiangVien giangvien =iterator.next();
                    String s= giangvien.getTen().toLowerCase();
                    if(s.equals(tenlow)){
                        iterator.remove();
                        found =true;
                    }
                }
                QuanLiMain.SaveData();
                if(found){
                    DefaultListModel model = (DefaultListModel) lsgv.getModel();
                    model.clear();
                    for(GiangVien o: listgv){
                        model.addElement(o.getTen()+" "+ o.getSdt());
                    }
                    lsgv.setModel(model);
                }else{
                    JOptionPane.showMessageDialog(null,"Không tìm thấy giảng viên"+ tengv,"Thông báo",JOptionPane.INFORMATION_MESSAGE);
                }
            }
           
            
        }

    }//GEN-LAST:event_jButton5ActionPerformed

